package uk.co.redfruit.libraries.srpDB.exceptions;

public class SRBClientException extends Exception {

	private static final long serialVersionUID = -578602838853525242L;

	public SRBClientException() {
		super();
	}

	public SRBClientException(String message, Throwable cause) {
		super(message, cause);
	}

	public SRBClientException(String message) {
		super(message);
	}

	public SRBClientException(Throwable cause) {
		super(cause);
	}
	
	

}
